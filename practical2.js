// Array Methods

// length()
let fruits = ["Banana", "Orange", "Apple", "Mango"];
console.log(fruits.length);

// toString()
console.log(fruits.toString())

// indexOf()
console.log(fruits.indexOf("Orange"))

// pop()
console.log(fruits.pop())
console.log(fruits)

// push()
console.log(fruits.push("Papaya"))

// sort()
console.log(fruits.sort())

